package com.praktek;

public class RukunWarga {
	private int rw;
	private String ketuaRw;
	
	public RukunWarga(int rw, String ketuaRw) {
		this.rw = rw;
		this.ketuaRw = ketuaRw;
	}
	
	public String getKetuaRw() {
		return ketuaRw;
	}
	public void setKetuaRw(String ketuaRw) {
		this.ketuaRw = ketuaRw;
	}
	public int getRw() {
		return rw;
	}
	public void setRw(int rw) {
		this.rw = rw;
	}
	
	
}
